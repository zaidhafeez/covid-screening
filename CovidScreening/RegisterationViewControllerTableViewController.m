- (void)viewDidAppear:(BOOL)animated {
    
    [super viewDidAppear: animated];
    
    NSManagedObjectContext *managedObjectContext = [self managedObjectContext];
    
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] initWithEntityName:@"Detail"];
    
    self.RegistrationDetails = [[managedObjectContext executeFetchRequest:fetchRequest error:nil] mutableCopy];
    
    [self.tableView reloadData];
    
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    
    return self.RegistrationDetails.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    NSManagedObject *registeration = [self.RegistrationDetails objectAtIndex:indexPath.row];
    [cell.textLabel setText: [NSString stringWithFormat:@"%@ %@", [registeration valueForKey:@"name"],[registeration valueForKey:@"mobileNumber"]]];
    [cell.detailTextLabel setText:[registeration valueForKey:@"country"]];
    
    return cell;
}

